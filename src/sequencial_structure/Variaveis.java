package sequencial_structure;

public class Variaveis {
    public static void main(String[] args) {
//        CALCULANDO A AREA DO TRAPEZIO
        double b, B, h, area;
        b = 6;
        B = 8;
        h = 5;
        area = (b + B) / 2 * h;
        System.out.println(area);

        int o, p;
        double result;
        o = 5;
        p = 2;
        result = (double) o / p;
        System.out.println(result);
    }
}
